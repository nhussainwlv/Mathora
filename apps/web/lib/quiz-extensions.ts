type QuizQuestion = {
  id: string;
  prompt: string;
  options: string[];
  answerIndex: number;
  explanation: string;
};

type Raw = [prompt: string, options: string[], answerIndex: number, explanation: string];

function build(id: string, items: Raw[]): QuizQuestion[] {
  return items.map(([prompt, options, answerIndex, explanation], i) => ({
    id: `${id}-q${i + 1}`,
    prompt,
    options,
    answerIndex,
    explanation,
  }));
}

