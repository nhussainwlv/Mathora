export function buildFullQuiz(topicId: string, base: QuizQuestion[]): QuizQuestion[] {
  const extra = quizExtensions[topicId] ?? [];
  const merged = [
    ...base.map((q, i) => ({ ...q, id: `${topicId}-q${i + 1}` })),
    ...build(topicId, extra).map((q, i) => ({ ...q, id: `${topicId}-q${base.length + i + 1}` })),
  ];
  return merged.slice(0, 10);
}
